document.querySelectorAll('.details-button').forEach(button => {
  button.addEventListener('click', event => {
    const card = event.target.closest('.product-card');
    const id = card.dataset.id;
    const details = productDetails[id];
    if (details) {
      showDetails(details);
    }
  });
});

